     public void decrypt()

    {
        String str =jTextArea2.getText();
        String key1=jTextField2.getText();
        int key=Integer.parseInt(key1);

        String decrypted = "";

        for(int i = 0; i < str.length(); i++)

        {

            int c = str.charAt(i);

            if (Character.isUpperCase(c))

            {

                c = c - (key % 26);

                if (c < 'A')

                c = c + 26;

           }

            else if (Character.isLowerCase(c))

            {

                c = c - (key % 26);

                if (c < 'a')

                c = c + 26;

            }
            decrypted += (char) c;

        }
        jTextArea1.setText(decrypted);
          }
