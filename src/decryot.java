    public static int posA = 0;
    public static int posB = 0;
    public static int finPos = 0;
    public static String add(char a, char b){
        l1 = String.format("%c",a); //convert the characters
        l2 = String.format("%c",b); //to a String
        for(int i = 0; i < alpha.length; i++){
            if(l1.equals(alpha[i])){
                posA = i; //finds the index of character a
            }
        }
        for(int j = 0; j < alpha.length; j++){
            if(l2.equals(alpha[j])){
                posB = j; //index of character b
            }
        }
        if(posA + posB <= 25){ //if the index of the "sum" is between 0 and 25
                               //it could go over
            finPos = posA + posB;
        }
        else{ //if it went over
            finPos = posA + posB - 26; //subtract!
                                       //a minimum index of 26, so subtract 26
        }
        return alpha[finPos]; //return the "sum" as a String
    }
